    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nifCif = jTextField1.getText();
        double cantidad = (double) jSpinner1.getValue();
        String concepto = jTextField2.getText();
        String observaciones = jTextArea1.getText();
        Date fecha = Date.valueOf(LocalDate.now());

        try {
            Gestor.getInstance().checkNifCif(nifCif);
            try {
                Usuario user = Gestor.getInstance().getUser(nifCif);
                Movimiento m = new Movimiento(user, cantidad, fecha, concepto, observaciones);
                Gestor.getInstance().addMovimiento(m);
                JOptionPane.showMessageDialog(this, "Se ha a√±adido correctamente.");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } catch (AppException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

